export type TranslationKey = keyof typeof translations.en;

/**
 * Translation helper function.
 * Usage: t('nav.home', language)
 */
export function t(key: TranslationKey, lang: 'en' | 'es' = 'en'): string {
  return (translations[lang] as Record<string, string>)[key] ?? (translations.en as Record<string, string>)[key] ?? key;
}

/**
 * Format a translation key with parameter substitution.
 * Replace {0}, {1}, etc. with provided values.
 */
export function tf(key: TranslationKey, lang: 'en' | 'es' = 'en', ...args: (string | number)[]): string {
  let text = t(key, lang);
  args.forEach((val, i) => {
    text = text.replace(`{${i}}`, String(val));
  });
  return text;
}
